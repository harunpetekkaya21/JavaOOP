public class Ceiling {
    private int height;
    private int painttedColor;

    public Ceiling(int height, int painttedColor) {
        this.height = height;
        this.painttedColor = painttedColor;
    }

    public int getHeight() {
        return height;
    }

    public int getPainttedColor() {
        return painttedColor;
    }
}
